public class ExistMappingException extends Exception {
    private static final long serialVersionUID = 1L;

    public ExistMappingException() {
        super();
    }

    public ExistMappingException(String msg) {
        super(msg);
    }

    public ExistMappingException(Throwable throwable) {
        super(throwable);
    }

    public ExistMappingException(String msg, Throwable throwable) {
        super(msg,throwable);
    }

    public void printStackTrace() {
        super.printStackTrace();
    }
}
